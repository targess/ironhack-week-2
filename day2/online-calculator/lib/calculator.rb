class Calculator

	def self.add(first_number, second_number)
		first_number + second_number
	end

	def self.substract(first_number, second_number)
		first_number - second_number
	end

	def self.multiply(first_number, second_number)
		first_number * second_number
	end

	def self.divide(first_number, second_number)
		first_number / second_number		
	end
end